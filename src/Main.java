        public int [] bubbleSort(int[]arr){
            for (int i = 0; i <arr.length ; i++) {
                for (int j = 0; j <arr.length ; j++) {
                    if(arr[j]<arr[i]){
                        int temp = arr[j];
                        arr[i] = arr[j];
                        arr[j] = temp;
                    }
                }
            }
            return arr;
        }


